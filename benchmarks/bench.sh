#!/usr/bin/env bash
# Compare Oryn vs Lua on equivalent programs. We target plain Lua
# because that's what game mods and embedded scripts use in practice;
# LuaJIT is a different product with different tradeoffs and isn't
# the benchmark we care about.
#
# Modes:
#   source  - end-to-end CLI benchmark: process startup, file loading,
#             parsing/compilation, and execution are all included
#   runtime - runtime-focused benchmark: each script does the
#             measured work once in one process
#   all     - run both modes

set -euo pipefail

ORYN="./target/release/oryn"
MODE="all"
ALL_PROGRAMS=(fib sum_loop call_loop field_update method_loop range_loop)
REPORT_FILE="benchmarks/report.md"
README_FILE="README.md"
SUMMARY_FILE="$(mktemp)"

# Build Oryn in release mode if needed.
if [ ! -f "$ORYN" ]; then
    echo "Building Oryn (release)..."
    (cargo build --release --bin oryn)
fi

programs=()

while [ "$#" -gt 0 ]; do
    case "$1" in
        --mode)
            MODE="${2:-}"
            shift 2
            ;;
        --mode=*)
            MODE="${1#--mode=}"
            shift
            ;;
        *)
            programs+=("$1")
            shift
            ;;
    esac
done

if [ "${#programs[@]}" -eq 0 ]; then
    programs=("${ALL_PROGRAMS[@]}")
fi

cleanup() {
    rm -f "$SUMMARY_FILE"
}

trap cleanup EXIT

rm -f "$REPORT_FILE"
printf '# Benchmark Report\n\n' >"$REPORT_FILE"
printf 'Generated by `./benchmarks/bench.sh`.\n\n' >>"$REPORT_FILE"

case "$MODE" in
    source) modes=(source) ;;
    runtime) modes=(runtime) ;;
    all) modes=(source runtime) ;;
    *)
        echo "Unknown mode: $MODE"
        echo "Expected one of: source, runtime, all"
        exit 1
        ;;
esac

for mode in "${modes[@]}"; do
    case "$mode" in
        source)
            oryn_root="benchmarks/source/oryn"
            lua_root="benchmarks/source/lua"
            mode_label="end-to-end CLI"
            ;;
        runtime)
            oryn_root="benchmarks/runtime/oryn"
            lua_root="benchmarks/runtime/lua"
            mode_label="runtime-focused"
            ;;
    esac

    printf '## %s\n\n' "$mode" >>"$REPORT_FILE"

    for prog in "${programs[@]}"; do
        oryn_file="${oryn_root}/${prog}.on"
        lua_file="${lua_root}/${prog}.lua"

        if [ ! -f "$oryn_file" ]; then
            echo "Skipping $prog ($mode): $oryn_file not found"
            continue
        fi
        if [ ! -f "$lua_file" ]; then
            echo "Skipping $prog ($mode): $lua_file not found"
            continue
        fi

        echo ""
        echo "=== $prog ==="
        echo ""
        echo "Mode: $mode_label"
        echo ""

        temp_report="$(mktemp)"

        hyperfine \
            --warmup 3 \
            --runs 10 \
            --reference "$ORYN run $oryn_file" \
            --export-markdown "$temp_report" \
            "lua $lua_file"

        mapfile -t mean_values < <(
            awk -F'|' '
                /^\| `/ {
                    field = $3
                    gsub(/^ +| +$/, "", field)
                    split(field, parts, " ")
                    print parts[1]
                }
            ' "$temp_report"
        )

        if [ "${#mean_values[@]}" -eq 2 ]; then
            printf '%s %s %s\n' \
                "$mode" \
                "$(awk "BEGIN { printf \"%.12f\", ${mean_values[0]} / ${mean_values[1]} }")" \
                "$prog" >>"$SUMMARY_FILE"
        fi
        printf '### %s\n\n' "$prog" >>"$REPORT_FILE"
        printf 'Mode: %s\n\n' "$mode_label" >>"$REPORT_FILE"
        cat "$temp_report" >>"$REPORT_FILE"
        printf '\n' >>"$REPORT_FILE"
        rm -f "$temp_report"
    done
done

summary_text="$(
    awk '
        function emit(label, lua_prod, count) {
            if (count == 0) {
                return
            }

            lua_gm = exp(log(lua_prod) / count)
            printf "- %s: across %d benchmarks, Oryn is about %.2fx slower than Lua.\n", label, count, lua_gm
        }

        BEGIN {
            overall_lua = 1
        }

        {
            mode = $1
            lua_ratio = $2

            overall_lua *= lua_ratio
            overall_count += 1

            if (!(mode in seen)) {
                seen[mode] = 1
                mode_order[++mode_count] = mode
                mode_lua[mode] = 1
                mode_items[mode] = 0
            }

            mode_lua[mode] *= lua_ratio
            mode_items[mode] += 1
        }

        END {
            if (overall_count == 0) {
                exit
            }

            print "## Summary"
            print ""
            emit("Overall", overall_lua, overall_count)

            for (i = 1; i <= mode_count; i += 1) {
                mode = mode_order[i]
                label = toupper(substr(mode, 1, 1)) substr(mode, 2)
                emit(label, mode_lua[mode], mode_items[mode])
            }
        }
    ' "$SUMMARY_FILE"
)"

if [ -n "$summary_text" ]; then
    temp_report="$(mktemp)"
    {
        printf '%s\n\n' "$summary_text"
        cat "$REPORT_FILE"
    } >"$temp_report"
    mv "$temp_report" "$REPORT_FILE"
fi

# Update the ## Benchmarks section in README.md
if [ -n "$summary_text" ] && [ -f "$README_FILE" ]; then
    summary_tmp="$(mktemp)"
    printf '%s\n' "$summary_text" | sed '/^## Summary$/d' | sed '/./,$!d' >"$summary_tmp"
    SUMMARY_TMP="$summary_tmp" awk '
        /^## .*[Bb]enchmarks/ {
            print
            print ""
            while ((getline line < ENVIRON["SUMMARY_TMP"]) > 0) {
                print line
            }
            print ""
            # Skip old content until the next ## header or EOF
            while ((getline line) > 0) {
                if (line ~ /^## /) {
                    print line
                    break
                }
            }
            next
        }
        { print }
    ' "$README_FILE" > "${README_FILE}.tmp" && mv "${README_FILE}.tmp" "$README_FILE"
    rm -f "$summary_tmp"
    echo ""
    echo "Updated ## Benchmarks section in $README_FILE"
fi

echo ""
echo "Wrote consolidated report to $REPORT_FILE"
